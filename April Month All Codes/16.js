let c = 34;
let d = 56;

console.log(c != d); // true
console.log(c == d); // false

console.log(c > d); // false
console.log(c < d); // true
console.log(c >= d); // false
console.log(c <= d); // true

let str = "134";
let num = 134;
console.log(str == num); // Loosely checking // true

console.log(str === num );// With Data Type Strict Checking // false 
console.log(str !== num);// True



