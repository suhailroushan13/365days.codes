// Bitwise Operator

let a = 3;
let b = 2;

console.log(a & b); // 2 
console.log(a | b); // 3

// Bit of a = 3 is 0011 
// Bit of b = 2 is 0010
// ----------------------
//                 0010  = 2 

// Bit OR a = 3 = 0011
// Bit OR b = 2 = 0010
// ----------------------
//                 0011 = 3





